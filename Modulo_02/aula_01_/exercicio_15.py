"""15. Simples pop()
Crie uma lista com 5 tarefas. Use o método .pop() para remover a última tarefa da lista.
Imprima a tarefa que foi removida e a lista atualizada"""

tarefas = ["fazer compras", "academia", "trabalho", "limpar casa", "fazer unhas"]
removido = tarefas.pop(1)
print(removido)
print(tarefas)