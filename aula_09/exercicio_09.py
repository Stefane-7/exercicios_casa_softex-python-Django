"""9. sort() e reverse()
Crie uma lista com 5 nomes de cidades (não em ordem alfabética). Use o método .sort() para
ordenar a lista. Depois, use .reverse() para inverter a ordem. Imprima a lista nos dois"""
cidades = ["maricá", "são gonçalo", "lapa", "niteroi","bom-sucesso"]
